
# Quatro operações

n1 = float(input("Primeiro valor:"))
n2 = float(input("Segundo valor:"))

soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2

print("A soma de {} mais {} é igual a {}".format(n1,n2,soma))
print("A subtração de {} menos {} é igual a {}".format(n1,n2,sub))
print("A multiplicação de {} vezes {} é igual a {}".format(n1,n2,mult))
print("A divisão de {} dividido por {} é igual a {}".format(n1,n2,div))